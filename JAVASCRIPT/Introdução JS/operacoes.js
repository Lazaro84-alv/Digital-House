//adição
console.log(2 + 2);

//subtração
console.log(10 - 5);

//multiplicação
console.log(10 * 5);

//divisão
console.log(30 / 2);

//modulo(resto da divisão)
console.log(15 % 2);

//incremento
let numero = 10;
console.log(++numero);

//decremento
console.log(--numero);

//comparação de maior
console.log(10 > 5);

//comparação de menor
console.log(10 < 5);

//comparação de menor ou igual
console.log(3 <= 2);

//comparação de maior ou igual
console.log(20 >= 20);

//comparação de igualdade
console.log(10 == 10);
console.log("Ola" == "Ola");

//comparação de igualdade rigida
console.log(5 === "5");

//comparação de diferente
console.log(10 != 20);

//comparação de diferença rigida
console.log(32 !== "23");

//Operadores Lógicos

//comparador lógico E &&
console.log(10 > 5 && 8 > 10 && 11 > 5);

//comparador lógico OR ||
console.log(10 > 5 || 8 > 10);

//comparador de negação !
console.log(!true);

//operador de concatenar
let nome = "Atum";
let sobrenome = "Picles";
console.log(nome + " " + sobrenome);


